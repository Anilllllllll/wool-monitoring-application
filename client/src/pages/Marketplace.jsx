import React, { useEffect, useState } from 'react';
import client from '../api/axiosClient';
import { useAuth } from '../context/AuthContext';
import { ShoppingBag, Search, Filter, Package } from 'lucide-react';
import { useNavigate } from 'react-router-dom';
import DashboardLayout from '../layouts/DashboardLayout';
import { Card } from '../components/ui/Card';
import { Button } from '../components/ui/Button';
import { Badge } from '../components/ui/Badge';
import { Input } from '../components/ui/Input';
import { Loader } from '../components/ui/Loader';

const Marketplace = () => {
    const { user } = useAuth();
    const [products, setProducts] = useState([]);
    const [loading, setLoading] = useState(true);
    const [searchTerm, setSearchTerm] = useState('');
    const [showFilters, setShowFilters] = useState(false);
    const [filterGrade, setFilterGrade] = useState('');
    const navigate = useNavigate();

    useEffect(() => {
        const fetchProducts = async () => {
            try {
                const { data } = await client.get('/shop/products');
                setProducts(data);
            } catch (error) {
                console.error("Error fetching products:", error);
            } finally {
                setLoading(false);
            }
        };
        fetchProducts();
    }, []);

    const handleBuy = async (batchId) => {
        if (!window.confirm("Confirm purchase of this batch?")) return;

        try {
            await client.post('/shop/order', { batchIds: [batchId] });
            navigate('/orders');
        } catch (error) {
            console.error("Purchase failed:", error);
            alert("Failed to purchase. It may have been sold.");
        }
    };

    const filteredProducts = products.filter(product => {
        const matchesSearch = product.woolType.toLowerCase().includes(searchTerm.toLowerCase()) ||
            (product.batchId && product.batchId.toLowerCase().includes(searchTerm.toLowerCase()));
        const matchesGrade = filterGrade ? product.qualityReport?.colorGrade === filterGrade : true;
        return matchesSearch && matchesGrade;
    });

    if (loading) return (
        <DashboardLayout role="Buyer">
            <div className="flex h-[80vh] items-center justify-center"><Loader size="xl" /></div>
        </DashboardLayout>
    );

    return (
        <DashboardLayout role="Buyer">
            <div className="space-y-8">
                {/* Header & Search */}
                <div className="flex flex-col md:flex-row justify-between md:items-center gap-6">
                    <div>
                        <h1 className="text-3xl font-bold text-white mb-2">Wool Marketplace</h1>
                        <p className="text-slate-400">Welcome, <span className="text-primary-400 font-semibold">{user?.name}</span>. Premium verified wool batches available for purchase.</p>
                    </div>

                    <div className="flex gap-3 w-full md:w-auto">
                        <div className="relative flex-[2] md:w-64">
                            <Search className="absolute left-3 top-3 text-slate-400" size={18} />
                            <input
                                type="text"
                                placeholder="Search types..."
                                value={searchTerm}
                                onChange={(e) => setSearchTerm(e.target.value)}
                                className="w-full pl-10 pr-4 py-2.5 bg-white/5 border border-white/10 rounded-xl text-white placeholder:text-slate-500 focus:outline-none focus:border-primary-500 focus:ring-1 focus:ring-primary-500/50 transition-all"
                            />
                        </div>
                        <Button
                            variant="secondary"
                            className={`flex items-center gap-2 ${showFilters ? 'bg-primary-500/20 text-primary-300 border-primary-500/50' : ''}`}
                            onClick={() => setShowFilters(!showFilters)}
                        >
                            <Filter size={18} /> <span className="hidden sm:inline">Filters</span>
                        </Button>
                    </div>
                </div>

                {/* Filters Panel */}
                {showFilters && (
                    <Card className="bg-surface/50 border-white/5 animate-in slide-in-from-top-2">
                        <p className="text-xs font-bold text-slate-500 uppercase tracking-wide mb-3">Filter by Grade</p>
                        <div className="flex flex-wrap gap-2">
                            {['A', 'B', 'C', 'D', 'E'].map(grade => (
                                <button
                                    key={grade}
                                    onClick={() => setFilterGrade(filterGrade === grade ? '' : grade)}
                                    className={`px-4 py-1.5 rounded-lg text-sm font-medium border transition-all ${filterGrade === grade
                                        ? 'bg-primary-500 text-white border-primary-500 shadow-neon'
                                        : 'bg-white/5 text-slate-400 border-white/10 hover:bg-white/10 hover:text-white'
                                        }`}
                                >
                                    Grade {grade}
                                </button>
                            ))}
                            {filterGrade && (
                                <button
                                    onClick={() => setFilterGrade('')}
                                    className="px-3 py-1 text-sm text-red-400 hover:text-red-300"
                                >
                                    Clear
                                </button>
                            )}
                        </div>
                    </Card>
                )}

                {/* Product Grid */}
                <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-6">
                    {filteredProducts.map(product => (
                        <Card key={product._id} className="group p-0 overflow-hidden border-white/5 hover:border-primary-500/30 transition-all duration-300">
                            {/* Image Header */}
                            <div className="h-56 relative overflow-hidden bg-surfaceHighlight">
                                {product.images && product.images.length > 0 ? (
                                    <>
                                        <div className="absolute inset-0 bg-gradient-to-t from-surface to-transparent opacity-80 z-[1]" />
                                        <img
                                            src={`http://localhost:5000${product.images[0]}`}
                                            alt={product.woolType}
                                            className="w-full h-full object-cover group-hover:scale-110 transition-transform duration-700"
                                        />
                                    </>
                                ) : (
                                    <div className="w-full h-full flex items-center justify-center text-slate-600">
                                        <Package size={48} className="opacity-20" />
                                    </div>
                                )}

                                <div className="absolute top-3 right-3 z-10">
                                    <Badge variant="success" className="shadow-lg backdrop-blur-md">
                                        Grade {product.qualityReport?.colorGrade || 'A'}
                                    </Badge>
                                </div>

                                <div className="absolute bottom-0 left-0 right-0 p-4 z-10">
                                    <h3 className="text-xl font-bold text-white mb-1 group-hover:text-primary-300 transition-colors">{product.woolType}</h3>
                                    <p className="text-xs text-slate-400 font-mono">#{product.batchId || product._id.slice(-6)}</p>
                                </div>
                            </div>

                            {/* Details Body */}
                            <div className="p-5 space-y-4">
                                <div className="text-center p-3 bg-white/5 rounded-xl border border-white/5">
                                    <p className="text-xs text-slate-500 uppercase">Estimated Price</p>
                                    <p className="text-2xl font-bold text-white font-mono bg-clip-text text-transparent bg-gradient-to-r from-emerald-400 to-teal-400">
                                        ${(product.weight * 15).toLocaleString()}
                                    </p>
                                </div>

                                <div className="grid grid-cols-2 gap-4 text-sm">
                                    <div>
                                        <p className="text-slate-500 text-xs">Weight</p>
                                        <p className="font-semibold text-white">{product.weight}kg</p>
                                    </div>
                                    <div className="text-right">
                                        <p className="text-slate-500 text-xs">Micron</p>
                                        <p className="font-semibold text-primary-400">{product.qualityReport?.fiberDiameter || "N/A"}Âµm</p>
                                    </div>
                                </div>

                                <Button
                                    onClick={() => handleBuy(product._id)}
                                    className="w-full mt-2 shadow-neon bg-gradient-to-r from-primary-600 to-indigo-600 border-none"
                                >
                                    <ShoppingBag size={16} className="mr-2" /> Buy Now
                                </Button>
                            </div>
                        </Card>
                    ))}

                    {filteredProducts.length === 0 && (
                        <div className="col-span-full py-20 text-center border-2 border-dashed border-white/10 rounded-2xl">
                            <ShoppingBag size={48} className="mx-auto text-slate-600 mb-4" />
                            <h3 className="text-xl font-bold text-white mb-2">No products found</h3>
                            <p className="text-slate-400">Try adjusting your search criteria.</p>
                            {(searchTerm || filterGrade) && (
                                <button
                                    onClick={() => { setSearchTerm(''); setFilterGrade(''); }}
                                    className="mt-4 text-primary-400 hover:text-primary-300 text-sm font-medium"
                                >
                                    Clear all filters
                                </button>
                            )}
                        </div>
                    )}
                </div>
            </div>
        </DashboardLayout>
    );
};

export default Marketplace;
